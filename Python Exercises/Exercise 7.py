#Each for loop has a message for each for loop indicating the increments or decrements up to a number

print("This loop prints 1 to 50 in increments of 1")
for Num1 in range(1, 51):
    print(Num1)

print("\n")
print("This loop prints 50 to 1 in decrements of 1")
for Num2 in range(50, 0, -1):
    print(Num2)

print("\n")
print("This loop prints 30 to 50 in increments of 1")
for Num3 in range(30, 51):
    print(Num3)

print("\n")
print("This loop prints 50 to 10 in decrements of 2")
for Num4 in range(50, 9, -2):
    print(Num4)

print("\n")
print("This loop prints 100 to 200 in increments of 5")
for Num5 in range(100, 201, 5):
    print(Num5)